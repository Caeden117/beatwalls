fun sideWave(): ArrayList<Wall> {
    val list = arrayListOf<Wall>()
    val max = 12.0
    for(i in 0 until (max).roundToInt()){
        val y = i/max*(2* PI)
        val nY = (i+1)/max*(2* PI)

        list.add(
            Wall(
               duration =  1 / max,
                height = abs(cos(nY)- cos(y)),
                startHeight = 1-cos(y),
                startRow = 3.0,
                width = 0.5,
                startTime = i/max
            )
        )
    }
    return list
}

